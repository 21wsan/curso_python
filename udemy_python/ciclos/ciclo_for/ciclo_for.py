print('*** Ciclo For en Python ***')

print('\nRecorrer o iterar los caracteres de una cadena')
cadena = 'Hola Mundo'
for letra in cadena:
    print(letra, end = ' ')

# lista de frutas
print('\nRecorrer o iterar la lista de frutas')
frutas = ['Fresa', 'Mango', 'Naranja']
for fruta in frutas:
    print(fruta, end=' - ')