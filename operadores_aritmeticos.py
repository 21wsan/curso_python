a=8
b=5

# suma
c=a+b
print("resultado de sumar 8+5 =",c)

# resta

c=a-b
print("resultado de restar 8-5 =",c)

# multiplicacion

c=b*2
print("resultado de multiplicar 5*2 =",c)

# division

c=a/b
print("resultado de dividir 8/5 =",c)

# modulo o residuo de la division

c=a%b
print("modulo de 8%5 =",c)

# potencia: x elevado a la potencia y

c=a**b
print("resultado de elevar 8**5 =",c)

# // devuelve la parte entera del cociente

c=a//b
print("devuelve la parte entera del cociente 8//5 =",c)