# impresion de cadenas
frase1="El perro"
frase2="Manchas"
frase3="ladra en la oscuridad"

# el + concatena cadenas

print(frase1+" "+frase2+" "+frase3+"?")

# el * repite cadenas

cadena1=frase2*3
print(cadena1)

# cadena.capitalize() inicia con mayuscula

print(frase3.capitalize())

# cadena.center(ancho) centra el ancho dado

print(frase1.center(18))

# cadena.lower pasa todo a minusculas

print(frase2.lower())

# cadena.upper pasa todo a mayusculas

print(frase2.upper())

# cadena.title Mayusculas iniciales

print(frase3.title())