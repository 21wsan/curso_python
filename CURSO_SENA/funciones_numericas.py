# range() = crea un rango de numeros

x= range(6)
print(list(x))

# str() = convierte un valor numerico a texto

str(100)

# int() = convierte a valor entero

int('89')

# float() = conviente a valor decimal

float('3.14')

# max() = determina el maximo dentro de un grupo de numeros

x=[5,6,7,3,54,5,7,8,7,6,4,3,24,324,244,3434]
print(max(x))

# min() = determina el minimo dentro de un grupo de numero
print(min(x))

# sun() = suma el total de numeros de una lista
print(sum(x))

# OTRAS FUNCIONES EN PYTHON

# list() = crea una lista a partir de un elemento
z = range(10)
print(list(z))

# ord() = devuelve el valor ASCII de una cadena o caracter
print(ord('W'))

# round() = redondea despues de la coma de un float
print(round(15.92))